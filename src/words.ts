// Function to get random words for a game session
export function getRandomWords(count: number, bdpOnly?: boolean): Word[] {
  let filtered = wordList;

  if (bdpOnly) {
    // Only include words that contain b, d, or p
    filtered = wordList.filter(w => w.letters.length > 0);
  }

  const shuffled = [...filtered].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
